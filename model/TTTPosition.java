package pl.com.sda.pawel.machos.programowanieII_Rafal.model;

public class TTTPosition {
    private final int x;
    private final int y;

    public TTTPosition(int x, int y) {
        this.y = y;
        this.x = x;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
